import UIKit

extension UIColor {
    struct CustomColors {
        static let darkGray = UIColor(named: "customDarkGrayColor") ?? UIColor()
        static let gray = UIColor(named: "customGrayColor") ?? UIColor()
        static let green = UIColor(named: "customGreenColor") ?? UIColor()
        static let lightGray = UIColor(named: "customLightGrayColor") ?? UIColor()
        static let white = UIColor(named: "customWhiteColor") ?? UIColor()
        static let yellow = UIColor(named: "customYellowColor") ?? UIColor()
        static let mainBackground = UIColor(named: "mainBackground") ?? UIColor()
        static let textPrimaryColor = UIColor(named: "customPrimaryColor") ?? UIColor()
        static let textSecondaryColor = UIColor(named: "customSecondaryColor") ?? UIColor()
    }
}
